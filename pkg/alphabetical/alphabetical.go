package alphabetical

import (
	"github.com/anz-bank/sysl/pkg/sysl"
	"sort"
)

func Attribute(m map[string]*sysl.Attribute) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

